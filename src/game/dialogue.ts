function voiceOf(charId: CharacterId): Voice {
  return VOICES[charId] ?? ESTELLE; // 미등록 캐릭터 폴백 (루트 잠금 상태라 실사용 없음)
}

// 발화 결과 — id는 수집(들은 대사) 기록용 안정 키
export interface SpokenLine { id: string; text: string }

export function greeting(charId: CharacterId, tier: number): SpokenLine {
  const dp = daypart();
  const pool = voiceOf(charId).greetings[dp];
  const ti = Math.min(tier, pool.length - 1);
  const vi = Math.floor(Math.random() * pool[ti].length);
  return { id: `g:${dp}:${ti}:${vi}`, text: pool[ti][vi] };
}

export function talkLine(charId: CharacterId, tier: number): SpokenLine {
  const pool = voiceOf(charId).talk;
  const ti = Math.min(tier, pool.length - 1);
  const vi = Math.floor(Math.random() * pool[ti].length);
  return { id: `t:${ti}:${vi}`, text: pool[ti][vi] };
}

export function giftLine(charId: CharacterId): SpokenLine {
  const pool = voiceOf(charId).gift;
  const vi = Math.floor(Math.random() * pool.length);
  return { id: `f:${vi}`, text: pool[vi] };
}

export function noTalkLeftLine(charId: CharacterId): string {
  return voiceOf(charId).noTalkLeft;
}

// ── 대사 도감 카탈로그 (수집 화면용 전체 목록 — id는 발화 id와 동일 규약) ──
// paid: 플레이로 듣는 대사가 아니라 코인으로 구매해 해금하는 혼잣말
export interface CatalogLine {
  id: string; text: string;
  cat: "인사" | "대화" | "선물" | "혼잣말";
  paid?: boolean;
}
const DAYPARTS: Daypart[] = ["morning", "afternoon", "evening", "night"];

export function lineCatalog(charId: CharacterId): CatalogLine[] {
  const v = VOICES[charId];
  if (!v) return []; // 보이스 미등록(루트 준비 중) — 도감에 '준비 중' 표시
  const out: CatalogLine[] = [];
  for (const dp of DAYPARTS)
    v.greetings[dp].forEach((tierLines, ti) =>
      tierLines.forEach((text, vi) => out.push({ id: `g:${dp}:${ti}:${vi}`, text, cat: "인사" })));
  v.talk.forEach((tierLines, ti) =>
    tierLines.forEach((text, vi) => out.push({ id: `t:${ti}:${vi}`, text, cat: "대화" })));
  v.gift.forEach((text, vi) => out.push({ id: `f:${vi}`, text, cat: "선물" }));
  v.monologue.forEach((text, vi) => out.push({ id: `m:${vi}`, text, cat: "혼잣말", paid: true }));
  return out;
}
