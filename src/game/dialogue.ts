type Daypart = "morning" | "afternoon" | "evening" | "night";

function daypart(): Daypart {
  const h = new Date().getHours();
  if (h < 6) return "night";
  if (h < 12) return "morning";
  if (h < 18) return "afternoon";
  return "evening";
}

