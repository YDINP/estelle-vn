export function greeting(tier: number): string {
  const pool = GREETINGS[daypart()];
  const line = pool[Math.min(tier, pool.length - 1)];
  return line[Math.floor(Math.random() * line.length)];
}

export function talkLine(tier: number): string {
  const pool = TALK_LINES[Math.min(tier, TALK_LINES.length - 1)];
  return pool[Math.floor(Math.random() * pool.length)];
}

export function giftLine(): string {
  return GIFT_LINES[Math.floor(Math.random() * GIFT_LINES.length)];
}

